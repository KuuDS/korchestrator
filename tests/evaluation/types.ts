import type { AgentRole, Plan } from "../../src/types.js";

/**
 * Evaluation dimensions for orchestration quality assessment.
 */
export type EvalDimension =
  | "planning"
  | "routing"
  | "parallelism"
  | "dependencies"
  | "replanning"
  | "aggregation"
  | "toolUsage";

/**
 * Weighted mapping for each evaluation dimension.
 */
export type DimensionWeight = Record<EvalDimension, number>;

/**
 * A single test scenario for evaluating the orchestrator.
 */
export interface Scenario {
  /** Unique kebab-case identifier */
  id: string;

  /** Human-readable scenario name */
  name: string;

  /** Detailed description of what the scenario tests */
  description: string;

  /** The user prompt sent to the planner */
  userRequest: string;

  /** Skills that should be used in the generated plan */
  expectedSkills: string[];

  /** Minimum number of tasks expected in the plan */
  expectedMinTasks: number;

  /** Maximum number of tasks expected in the plan */
  expectedMaxTasks: number;

  /** Whether the scenario requires search capabilities */
  requiresSearch: boolean;

  /** Optional expected dependency graph for validation */
  expectedDependencies?: Array<{ from: string; to: string }>;

  /** Complexity classification of the scenario */
  complexity: "simple" | "complex";

  /** Category of the user request */
  category: "information" | "analysis" | "code" | "recovery" | "synthesis";
}

/**
 * Score breakdown for a single evaluation dimension.
 */
export interface MetricScore {
  /** The dimension being scored */
  dimension: EvalDimension;

  /** Achieved score (0–100) */
  score: number;

  /** Maximum possible score for this dimension */
  maxScore: number;

  /** Detailed explanations for the scoring */
  details: string[];
}

/**
 * A single step in the execution trace.
 */
export interface ExecutionStep {
  /** Step sequence number */
  step: number;

  /** Action performed in this step */
  action: "classify" | "plan" | "route" | "execute" | "replan" | "aggregate";

  /** Optional task identifier involved */
  taskId?: string;

  /** Optional agent identifier involved */
  agentId?: string;

  /** Whether the step completed successfully */
  success: boolean;

  /** Optional additional details */
  details?: string;
}

/**
 * Result of running a single scenario through the orchestrator.
 */
export interface ScenarioResult {
  /** The scenario that was evaluated */
  scenario: Scenario;

  /** The plan generated by the orchestrator (null if planning failed) */
  planGenerated: Plan | null;

  /** Complexity classification returned by the orchestrator */
  classification: "simple" | "complex" | null;

  /** Routing decisions made for each task */
  routingDecisions: Array<{
    taskId: string;
    agentId: string;
    expectedAgentId: string;
  }>;

  /** Per-dimension metric scores */
  metricScores: MetricScore[];

  /** Total weighted score achieved */
  totalScore: number;

  /** Maximum possible weighted score */
  maxPossibleScore: number;

  /** Ordered trace of execution steps */
  executionTrace: ExecutionStep[];

  /** Total execution duration in milliseconds */
  durationMs: number;

  /** Whether the scenario passed overall evaluation */
  passed: boolean;
}

/**
 * Letter grade used in evaluation reports.
 */
export type GradingScale = "A" | "B" | "C" | "D" | "F";

/**
 * Threshold definitions for each grade.
 */
export const GRADE_THRESHOLDS: Record<
  GradingScale,
  { minScore: number; description: string }
> = {
  A: { minScore: 90, description: "Excellent" },
  B: { minScore: 80, description: "Good" },
  C: { minScore: 70, description: "Acceptable" },
  D: { minScore: 60, description: "Needs Improvement" },
  F: { minScore: 0, description: "Unacceptable" },
};

/**
 * Complete evaluation report aggregating all scenario results.
 */
export interface EvaluationReport {
  /** ISO-8601 timestamp when the evaluation was run */
  timestamp: string;

  /** Total number of scenarios evaluated */
  totalScenarios: number;

  /** Number of scenarios that passed */
  passedScenarios: number;

  /** Overall weighted score across all scenarios */
  overallScore: number;

  /** Maximum possible overall score */
  maxPossibleScore: number;

  /** Average score per dimension */
  dimensionAverages: Record<EvalDimension, number>;

  /** Detailed results for each scenario */
  scenarioResults: ScenarioResult[];

  /** Human-readable summary of the evaluation */
  summary: string;
}
